package com.epam.kaliada.classes.aggregation.task2;

public class Wheel {
    private final String brand;
    private final double size;

    public Wheel(String brand, double size) {
        this.brand = brand;
        this.size = size;
    }

    public String getBrand() {
        return brand;
    }

    public double getSize() {
        return size;
    }
}
